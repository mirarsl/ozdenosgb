					<h2>How Does Risk Analysis Work?</h2>
					<p>In risk assessment, there are many ways to identify risks. Depending on the structure of the business, the characteristics of the processes, the tasks performed and the technology used, you can select a method among many. There is no coercion at this point.</p>
					<p>For many businesses, the 5-step approach provides both easy-to-understand and required guidance.</p>
					
					<h3>1-) Identification of Hazards</h3>
					<p>In this step, you need to determine what will harm the employees, the production and the operation.</p>
					<h3>2-) Evaluation of Hazards</h3>
					<p>The assessment of the list of hazards you have created in the first step determines what measures to take and what risk rating should be done for them.</p>
					<h3>3-) Risk rating</h3>
					<p>In the second step, the risk ratios are rated by calculating the weight ratios of the independent risks for each that are decided to be risk-rated, and the risks are subject to order of priority.</p>
					<h3>4-) Application of Control Measures</h3>
					<p>Necessary precautions are taken for hazards that can be removed immediately according to the measures decided in the second and third steps, and an appropriate control period is determined in order not to reoccur. Implementation plans for non-urgent measures which require a certain cost and time are prepared and put into practice.</p>
					<h3>5-) Auditing, Monitoring and Review</h3>
					<p>All stages and practices of risk management at workplaces are regularly audited, monitored and resolved aspects are reviewed.</p>


					
					<h2>Misconceptions Regarding Risk Assessment</h2>
					<p>The efficient risk assessment must be appropriate to provide the expected benefit. While the number of risk assessment companies is increasing, quality has become a debated concept for risk analysis. Here are the frequent mistakes impacting on quality and risk analysis that many companies have repeated:</p>
					<ul>
						<li>Risk analysis is considered as a one-time job. However, risk analysis is a process that needs to be updated on a regular basis. (Occupational Health and Safety Risk Assessment Regulation Article-12)</li>
						<li>Risks, working conditions are not analyzed and evaluated collectively. As a consequence, there is no effective result because of the lack of complementary measures and the lack of a holistic approach. (Occupational Health and Safety Risk Assessment Regulation Article-10)</li>
						<li>Risk assessment can not be well documented. (Occupational Health and Safety Risk Assessment Regulation Article-11)</li>
						<li>The risk assessment is performed superficially and focuses on the risks that are obvious and require urgent intervention. Long-term effects such as those stemming from chemicals, psychosocial risks, ergonomic factors are ignored.</li>
						<li>The effectiveness of the measures is not adequately monitored. (Occupational Health and Safety Risk Assessment Regulation Article-10/3)</li>
						<li>It can be realized by a competent person in the company, but it does not provide the desired result with the persons who do not know the firm and by realizing without the participation of the employees. (Occupational Health and Safety Risk Assessment Regulation Article-5 Risk Assessment Team should do it.)</li>
					</ul>
