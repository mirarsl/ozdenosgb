					<h2 class="first-header">İş Ekipmanları ve Periyodik Kontrol</h2>

					<p>Çalışma alanlarında bulunan pek çok iş ekipmanı periyodik kontrole tabidir. İş ekipmanı denildiğinde genel olarak üretim ve yapı sahaları akla geliyor olsa da aslında her türlü çalışma alanında çeşitli iş ekipmanları bulunmaktadır. Bunların büyük bir kısmı ise periyodik kontrole tabi ekipmanlardır.</p>
					<p>Periyodik kontrole tabi ekipmanlar ve şartlar ise İş Ekipmanlarının Kullanımında Sağlık ve Güvenlik Şartları Yönetmeliği ile belirlenmektedir.</p>
					<p>Öncelikle kavramları tanıyacak olursak;</p>

					<h2>İş Ekipmanı Nedir?</h2>
					<p>İşin yapılmasında ve yürütümünde kullanılan makine, alet, cihaz, teçhizat, tesis ve tesisat iş ekipmanıdır.</p>

					<h3>Periyodik Kontrol Nedir?</h3>
					<p>İş ekipmanlarının, yönetmelikte(a) öngörülen aralıklarda ve belirtilen yöntemlere uygun olarak yetkili kişilerce yapılan muayene ve/veya test faaliyetleridir.</p>

					<h3>Periyodik Kontroller Kimler Tarafından Yapılmalı?</h3>
					<p>İş ekipmanlarının teknik özelliklerinin gerektirdiği ve EKİPNET’e(b) kayıtlı ilgili branşlardan mühendis, teknik öğretmen, tekniker ve yüksek teknikerler periyodik kontrolleri yapmaya yetkili kişilerdir.</p>
					<h3>Neden Periyodik Kontrol Yapılmalı, Periyodik Kontrol Zorunlu mu?</h3>
					<p>İş ekipmanlarının yapısal bütünlüğünün, sağlamlığının ve güvenliğinin doğrulanması amacıyla ekipmanın ilgili standardına ve/veya imalatçının belirlediği teknik kriterlere uygunluğunun kontrolü için yönetmelik(a) kapsamında periyodik kontrol yapılmalıdır. Periyodik kontrole tabi iş ekipmanlarının kontrollerinin yapılması zorunludur.</p>
					<h4>Hangi İş Ekipmanları Periyodik Kontrole Tabi?</h4>
					<p>Periyodik kontrole tabi iş ekipmanları yönetmelikte(a) yer alan ek hüküm ve istisnalar saklı kalmak kaydıyla ana başlıklar halinde;</p>
					<ol>
						<li>Basınçlı kap ve tesisatlar</li>
						<li>Kaldırma ve iletme ekipmanları</li>
						<li>Tesisatlar</li>
