					<h2>Hastalıktan Genel Korunma Yöntemleri</h2>
					<ol>
						<li>Öksürme ve hapşırma sırasında ağzınızı ve burnunuzu bir mendil ile kapatınız.</li>
						<li>Mendilinizi kullandıktan sonra çöp sepetine atınız.</li>
						<li>Öksürdükten ve hapşırdıktan sonra ellerinizi bol sabun ve suyla yıkayınız. Alkol içeren el yıkama antiseptikleri de etkilidir.</li>
						<li>Kirli ellerinizle gözlerinize, burnunuza ve ağzınıza dokunmayınız.</li>
						<li>Grip belirtisi gösteren kişilere mesafeli (2 metre ve daha fazla) durunuz.</li>
						<li>Bulunduğunuz mekanı sık sık havalandırınız.</li>
						<li>Mümkün olduğunca üflemeli el kurutucularından ziyade kağıt havlu tercih ediniz.</li>
						<li>Salgın boyunca öpüşme, tokalaşma, kafa tokuşturmadan vazgeçiniz.</li>
						<li>Enfekte olmuş kişilerle yakın temastan kaçınmanın,</li>
						<li>El hijyenine dikkat etmenin, sık aralıklarla elleri en az 20 saniye sabun ve su ile yıkayın, sabun ve su olmadığı durumlarda alkol bazlı el dezenfektanı kullanın. </li>
						<li>Çiftlik veya vahşi hayvanlarla korunmasız temastan kaçınmanın.</li>
						<li>Enfekte olduysanız eğer, mesafeyi koruyun, öksürürken, hapşırırken tek kullanımlık kağıt mendil ile ağzınızı ve burnunuzu kapatın, kağıt mendilin bulunmadığı durumlarda ise dirsek içinizi kullanın, ellerin yıkamadan gözlerinize, burnunuza ve ağzınıza dokunmaktan kaçının.
						<li>Enfekte olan kişilerin dokunduğu yüzeyler dezenfekte edilmeden yüzeylere dokunmayın.</li>
					</ol>
					<h2>Hastalananların Yapacakları</h2>
					<p>Kendinizde yukarıda sıralanan hastalık belirtileri olduğunu hissederseniz hemen bir doktora başvurmalısınız. Doktorunuz herhangi bir teste ya da tedaviye ihtiyacınızın olup olmadığına karar verecektir. Eğer hastalandıysanız veya hastalık belirtilerini gösteriyorsanız evde istirahat ediniz ve çevrenizdeki kişilerden de onlara bulaştırmamak için uzak durunuz. Eğer işte hasta hissederseniz amirinize durumu hemen bildirin.</p>
					<p>Bulaşıcı virüs bulunan bir kişi ile temas eder ve kendinizden şüphe duymanız durumunda; İşyerinde iseniz, iş arkadaşlarınızdan uzak durmaya çalışın ve derhal işyeri hekimine başvurun. Evinizde aile bireylerinden kendinizi uzak tutmaya çalışın. Derhal bir sağlık kuruluşuna başvurun.</p>

					<h2>Koronavirüs Aşıları</h2>
					<p>Sağlık otoritelerince koronavirüs salgını için geliştirilen tek etkili korunma yöntemi olarak aşılama işaret edilmektedir. T.C. Sağlık Bakanlığı COVID-19 Aşısı Ulusal Uygulama Stratejisinde; COVID-19 pandemisine yanıt vermek amacıyla Bakanlık tarafından gerçekleştirilmesi planlanan müdahalelerden biri de kitlesel COVID-19 aşılaması olduğu, hastalığa maruz kalma, hastalığı ağır geçirme ve bulaştırma riskleri ile hastalığın toplumsal yaşamın işleyişi üzerindeki olumsuz etkisi değerlendirilerek COVID-19 aşısı uygulanacak gruplar belirlenmiş olup bu gruplara sırasıyla uygulanacağı belirtilmiştir.</p>
					<p>COVID-19 için birkaç farklı aşı türü bulunmakta ve geliştirilmektedir.</p>

					<p>Bu aşıların tümü vücudun bağışıklık sistemine COVID-19’a neden olan virüsü güvenli bir şekilde tanıtmayı ve yok etmeyi öğretecek şekilde tasarlanmıştır.</p>
					<ul>
						<li>Hastalığa neden olmayan ancak bağışıklık yanıtı oluşturan etkisizleştirilmiş virüs içeren aşılar (İnaktif aşılar).</li>
						<li>Hastalığa neden olmayan ancak bağışıklık yanıtı oluşturan zayıflatılmış virüs içeren aşılar (Canlı atenüe aşılar).</li>
						<li>Güvenli bir şekilde bağışıklık yanıtı oluşturmak için COVID-19 virüsünün yapısını taklit eden protein parçalarını kullanan protein bazlı aşılar.</li>
						<li>Güvenli bağışıklık yanıtı oluşturmak için COVID-19 virüsünün RNA parçacıklarını taşıyan hastalık yapıcı etkisi olmayan virüslerin kullanıldığı viral vektör aşıları.</li>
						<li>Kendi başına güvenli bağışıklık yanıtı oluşturan protein üretmek için genetik olarak tasarlanmış RNA ve DNA parçacıklarını kullanan son teknoloji bir yaklaşım olan m-RNA ve DNA aşıları.</li>
					</ul>
					<h2>Aşının Olası Yan Etkileri</h2>
					<p>T.C. Sağlık Bakanlığı yayınlarında bugüne kadar COVID-19 aşılarına yönelik gerek yürütülen klinik çalışmalarda gerekse mevcut aşı uygulamalarında ciddi yan etkilere rastlanmadığı ve aşılama sonrasında görülen yan etkiler sıklıkla hafif olduğu bildirilmiştir.</p>
					<p>Bu yan etkilerin; yorgunluk, baş ağrısı, ateş, titreme, kas/eklem ağrısı, kusma, ishal, aşı uygulanan bölgede ağrı, kızarıklık, şişlik gibi hafif yan etkiler olduğu, ancak nadir de olsa alerjik reaksiyonların olabileceği göz ardı edilmeden aşı uygulamasından sonra bir rahatsızlık hissedilmesi durumunda en yakın sağlık kuruluşuna başvurulması ve hekime yakın zamanda aşı olunduğu bilgisinin iletilmesi unutulmaması Sağlık Bakanlığınca vurgulanmaktadır.</p>

