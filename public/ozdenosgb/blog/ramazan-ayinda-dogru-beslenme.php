					<p>Ramazan ayı, oruç tutanlar için beslenme ve yaşam şeklinin değiştiği bir aydır. Yeterli ve dengeli beslenmenin sürdürülebilmesi için günün oruç tutulmayan bölümünde en az 2 öğünü tamamlamak ve sahur öğününü atlamamak gerekir. Sahura kalkılmaması ya da sahurda sadece su içilmesinin zararlı olduğu unutulmamalıdır. Çünkü bu beslenme tarzı yaklaşık 16 saat olan açlığı, ortalama 20 saate çıkarmaktadır. Bu da açlık kan şekerinin daha erken saatlerde düşmesine ve buna bağlı olarak günün daha verimsiz geçmesine neden olmaktadır. Bu durumun aksine eğer sahur öğünü, ağır yemeklerden oluşursa gece metabolizma hızı düştüğü için yemeklerin yağa dönüşme hızı ve kilo alma riski artmaktadır. Bu nedenle sahura mutlaka kalkılmalıdır.</p>
					<p>Sahurda süt, yoğurt, peynir, yumurta, tam tahıllı ekmekler gibi besinlerden oluşan hafif bir kahvaltı yapılabilir ya da çorba, zeytinyağlı yemekler, yoğurt ve salatadan oluşan bir öğün tercih edilebilir. Uzun süre tok tutması için rafine şekerden zengin ve liften fakir; tatlı, pasta benzeri gıdalar tercih edilmemelidir.</p>
					<p>Gün içerisinde aşırı acıkma problemi olanların midenin boşalma süresini uzatarak acıkmayı geciktiren kuru fasulye, nohut, mercimek, bulgur pilavı gibi yemekleri tüketmesi; aşırı yağlı, tuzlu ve ağır yemekler ile hamur işlerinden uzak durulması uygun olacaktır.</p>
					<p>İftar sofralarında bir insana yetecek yemeğin 2-3 kat fazlası bulunabilmektedir. İftarda kan şekeri çok düşük olduğundan kısa sürede çok miktarda besin tüketme isteği doğmaktadır. Yapılan en büyük hatalardan birisi de çok hızlı bir şekilde, çok yüksek miktarda besin tüketmektir. Çok hızlı yemek yendiğinde bu süre zarfında fazla miktarda, enerjisi yüksek besinler yenilir ve bu durum hem sağlık açısından risk oluşturabilir hem de ilerleyen günlerde kilo alımına zemin hazırlayabilir. İftar sonrası için planlanan yemekleri birkaç kısma ayırmak ve zamana yayarak tüketmek tokluk hissinin yönetimi için faydalı olacaktır. Bir diğer öneri de ramazanda porsiyonları küçültmek olabilir.</p>
					
					<h2>Sıvı Tüketimine Dikkat!</h2>

					<p>Sıcaklık ve nem artışına bağlı olarak vücut ısısı artmakta ve metabolizma bu yeni duruma uyum sağlamaya çalışmaktadır. Sıcaklıkların etkisiyle artan terleme ile birlikte yeterince sıvı alınmazsa su ve mineral kaybı sonucu, bayılma, bulantı, baş dönmesi gibi sağlık problemleri yaşanabilmektedir.</p>
					<p>Günde ortalama en az 2-2,5 litre (12-14 su bardağı) su içmeye, bununla birlikte Ramazan ayında sıvı ihtiyacını da karşılayacak ayran, taze sıkılmış meyve-sebze suları, sade soda vb. sık sık tüketmeye özen gösterilmelidir. Aynı zamanda birer idrar söktürücü olduğu bilinen çay, kahve gibi içecekler ile özellikle kavun, karpuz benzeri yaz meyvelerinin su ihtiyacını arttıracağı unutulmamalıdır.</p>
					
