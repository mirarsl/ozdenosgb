					<h4>1. Aydınlatma/Işık Şiddeti Ölçümü</h4>
					<p>Aydınlatma ölçümü hemen hemen her noktadan alınabilmektedir. Aydınlatma ölçümü için COHSR-928-1-IPG-039 isimli bir Kanada standardı kullanılmaktadır. İşyeri aydınlatmasının değerlendirilmesinde ise TS EN 12464 standardı kullanılır. Bu standart gereği çalışma ortamlarında insanların göz rahatlıkları için işin durumu ve alanın faaliyetleri göz önüne alınarak ölçümler gerçekleştirilir.</p>
					
					<h4>2. Termal Konfor Ölçümü</h4>
					<p>Termal konfor, rüzgar hızının, sıcaklığın ve ortamdaki nemin insanlar üzerinde oluşturduğu hissiyatı değerlendirmek için kullanılan bir ölçüm yöntemidir.
					
					<p><b>Termal Konforu Etkileyen Başlıca Faktöreler:</b></p>
					 
					<ul>
						<li>Hava sıcaklığı</li>
						<li>Hava akım hızı</li>
						<li>Havanın nem yoğunluğu</li>
						<li>Radyal ısı</li>
					</ul>
					 
					<p>Termal denge ise, vücudun iç ısı üretiminin, çevreye olan ısı kaybına eşitlenmesiyle elde edilir. Termal dengenin sağlanamadığı alanlar termal konfor ölçümünün yapılması gereken alanlardır.</p>
					<p>Kapalı ya da açık bir ortamda kötü şartlardan etkilenen kişilerin her biri için birer ölçüm gerçekleştirilebilir. Örneğin;</p>
					<ul>
						<li>Klimanın hava akımından direkt etkilenen kişiler,</li>
						<li>Kalorifer peteği ya da herhangi bir ısıtma kaynağına çok yakın oturan kişiler,</li>
						<li>Açık bir pencere, baca yanı ya da herhangi bir hava geçişine direk olarak maruz kalan kişiler.</li>
					</ul>
					
					<h4>3. Toz Ölçümü</h4>
					<p>Toz, çeşitli organik ve inorganik maddelerden aşınma, parçalanma, öğütme ve yanma sonucu oluşan, havada dağılma ve asılı kalma özelliklerine sahip katı fazda maddelerdir.</p>
