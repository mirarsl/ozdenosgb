					<p>The concept of work safety includes the protection of employees, the operator and production from all kinds of hazards and harms. Due to the priority of human life, it is seen that the security of operation and production is on the second plenary and the security of employees in general is expressed by the concept of international work safety.</p>
					<p>The World Health Organization (WHO) and the International Labor Organization (ILO) refer to Occupational Health and Safety as "to ensure the highest level of physical, mental and social well-being of workers in all occupations and to maintain this level, to prevent their health deterioration due to working conditions, To protect workers from the threats of unsanitary factors, to place workers in the most appropriate occupational environments for their physiological and psychological conditions, to place and maintain them in the most appropriate occupational environments, in summary, to ensure that human beings work and adapt to each person's own business ".</p>
					<p>The concept of Occupational Health and Safety differs from Worker Health and Occupational Safety concept in that it includes the determination, evaluation of risks and the work to be carried out to completely eliminate these risks or to minimize the damage. Business Health and Security in terms of international aspect; even when there is no danger and failure in the business, it includes determination of the danger and risks that may arise in the business and decision making works to decide whether they are acceptable or not, ie the old "reactive" approaches have left their place to "proactive" approaches.</p>
					<p>Risk analysis / risk assessment evaluation is one of the most effective methods of work health and safety approach applied in work areas. The provision of the managerial activeness provided by analyzing the risks that we may encounter at the workplace is one of the main branches of work health and safety studies.</p>
					<h2>Risk Analysis and Management</h2>
					<p>Risk analysis is a systematic approach developed to control the health and safety aspects of the workplace. The main objective is to identify and prioritize existing risks at the workplace, take appropriate measures to control risk by prioritizing elimination of them.</p>
					<p>Purpose of Occupational Health and Safety Risk Management is to establish an effective safety net to prevent the emergence of undetected hazards by gathering the most valid and accurate information possible about the causes of work accidents and occupational diseases and the factors affecting them. A good risk analysis ensures the detection of dangerous and invisible hazards and effective security precautions in terms of protection from accidents that may arise.</p>
					<p>When encounter a risk, the first thing to do is to determine the source event and the effects of the risk. Thus, it becomes easier to recognize the risk and you can practically determine what measures can be taken against the risk. It is also worth mentioning that poorly defined risks may lead to further risks.</p>
					<h3>What Information Should I Collect for Risk Analysis and How?</h3>					
					<p>What you need to know in order to evaluate the risks at your workplace and to know them;</p>
					<ul>
						<li>Your business location,</li>
						<li>General information about people working in the business (you should be more careful with your pregnant, young or disabled workers considering that they are more likely to be exposed to occupational hazards than usual.)</li>
						<li>What work equipment, materials and process you use,</li>
						<li>What kind of work you do,</li>
						<li>The hazards you have identified and their sources,</li>
						<li>The results of your current hazards,</li>
						<li>What protective measures are you using,</li>
						<li>Have there been any work accidents in the work before?</li>
						<li>Has anyone in the work ever had occupational disease?</li>
