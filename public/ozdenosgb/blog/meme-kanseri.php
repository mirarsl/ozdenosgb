					<h2 class="first-header">Nedir, nasıl önlenir, nasıl anlaşılır?</h2>
					<p>Gerek dünyada gerekse ülkemizde en sık izlenen ilk on kanser arasında ilk sıralarda yer alan meme kanseri, meme dokusunda yer alan hücrelerin kontrolsüz çoğalması ile ortaya çıkmaktadır. Ülkemizde meme kanseri her 4 kadın kanserinden birisi olmayı sürdürmektedir. Erkeklerde meme kanseri kadınlara oranla çok daha az izlenmekte ancak çok daha hızlı ilerlemektedir.</p>
					<h2>Erken Teşhis Hayat Kurtarır</h2>
					<p>Erken evrelerde tespit edilen meme kanserlerinin hem tedavileri daha başarılı olmakta hem de yaşam kalitesi önemli ölçüde artmaktadır. Bu sebeple, olası bir kanser gelişim sürecini erken evrede, henüz klinik bulgular ortaya çıkmadan tespit ederek kadınlarda meme kanserine bağlı ölüm hızını düşürmek mümkün olabilmektedir.</p>
					<p>Tüm kanserlerde olduğu gibi; sağlıklı beslenme ve yaşama alışkanlıklarının, fiziksel aktivitenin, sağlıklı kiloda olmanın, sigara ve alkol kullanımından uzak durmanın meme kanseri riskini azalttığı bilinmektedir. Emzirmenin meme kanserinden koruduğuna dair çok sayıda çalışma bulunduğu için annelerin bebeklerini en az 2 yıl süreyle emzirmeleri önerilmektedir.</p>
					<h2>Meme Kanseri Risk Faktörüleri</h2>
					<p>Meme kanseri risk faktörlerinin bazıları engellenebilir faktörlerdir. Fazla kilolu olmak, yeterli fiziksel aktivite yapmamak, menopoz sonrası hormon tedavisi almak, düzenli alkol kullanmak bunlara örnek olarak verilebilir. Bu faktörleri ortadan kaldırmaya çalışmak hastalığın görülme sıklığını azaltmaktadır. </p>
					<p>Bazı risk faktörleri ise değiştirilemeyecek faktörler olarak karşımıza çıkmaktadır.  Kadın olmak, yaş almak, genetik yatkınlık sahibi olmak, birinci derece akrabalarda (anne, kız kardeş, kız çocuk) meme kanseri görülmesi, kişinin bir memesinde kanser olması, yoğun meme dokusuna sahip olmak, 12 yaşından önce adet görmeye başlamak, 55 yaşından sonra menapoza girmek, göğüs bölgesine ışın tedavisi (radyoterapi) almış olmak bu faktörler arasında sayılabilir.</p>
					<p>Doğum yapmamış ya da ilk doğumunu 30 yaşından sonra yapmış olmanın ve doğum kontrol hapı/iğnesi kullanmanın da meme kanseri görülme sıklığını artırdığı bilinmektedir.</p>
					<p>Ailede meme kanseri varlığının riski arttırdığı bilinmesine rağmen, meme kanseri olan kadınların çoğunluğunda (yaklaşık 10 kişiden 8'inde) ailede meme kanseri öyküsü olmadığı unutulmamalıdır.</p>
					<p>Meme kanserini olabildiğince erken yakalamak, başarılı bir tedavi şansı sunar. Bunun için 40-69 yaş aralığında tüm kadınların 2 yılda bir mamografi çektirmesi, yılda bir kez meme muayenesi için doktora müracaat etmesi ve ayda bir kez de kendi kendine meme muayenesi yapması önerilmektedir. </p>
					<h3>Düzenli Muayene</h3>
					<p>Kadınların 20 yaşından sonra her ay kendi kendine meme muayenesi yapması önerilmektedir. Muayene, ayna karşısında her iki memenin görüntüsünün düzgün ve simetrik olup olmadığının kontrolü ile başlamalıdır. Sonrasında parmaklar meme başından başlayarak dairesel bir şekilde meme üzerinde gezdirilerek ele gelen bir yumru (kitle) olup olmadığı kontrol edilmelidir.  Meme kanserinin en sık rastlanan belirtisi ağrısız bir yumrunun (kitlenin) ele gelmesidir.  Ele gelen yumru (kitle) olsa da olmasa da aşağıdaki belirtiler de şüphe uyandırmalıdır.</p>
					<ul>
						<li>Meme başı ve derisinde çökme veya çekilme, deride kızarıklık ve ödem varlığı,</li>
						<li>Bir memenin tamamının veya bir kısmının şişmesi, </li>
						<li>Memede ya da meme ucunda ağrı,</li>
						<li>Meme derisinde kaşıntı, tahriş ve yanma,</li>
						<li>Memede portakal kabuğu görünümü,</li>
						<li>Tek taraflı meme ucunda akıntı (özellikle kanlı akıntı),</li>
						<li>Meme ya da meme ucu derisinde kızarıklık, kabuklaşma, kalınlaşma,</li>
						<li>Koltuk altında yumru (kitle), </li>
					</ul>

					<p>Bu belirtilerden herhangi biri meme kanseri dışındaki durumlardan da kaynaklanabilir. Ancak bu belirtiler görüldüğünde, bir meme polikliniğine veya genel cerrahi uzmanına muayene olunmalıdır. 
					<p>Kendi Kendine Meme Muayenesi (KKMM) meme kanseri erken tanısında önemli bir tarama yöntemidir. Memelerin normalde nasıl göründüğünü ve el ile kontrol sırasında nasıl hissedildiğini bilmek, meme sağlığının önemli bir parçasıdır. Ancak neyin aranacağını bilmek, düzenli mamografilerin ve diğer tarama testlerinin yapılmasının yerini tutamaz. </p>
