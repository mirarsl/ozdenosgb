					<p>Ortak sağlık ve güvenlik birimi - OSGB, şirketlerin, iş sağlığı ve güvenliği ihtiyaçlarını karşılamak için iş güvenliği uzmanı, işyeri hekimi ve sağlık personeli hizmetleri veren, Çalışma ve Sosyal Güvenlik Bakanlığı tarafından yetkilendirilen kurumlardır. 2005 yılında iş güvenliği danışmanlığı vermeye başlayan şirketimiz, 3 bine yakın osgb olan ülkemizde, halen aktif olarak çalışan ilk osgblerden biridir. Yüzden fazla iş güvenliği uzmanı ve işyeri hekimimizle, beş yüzden fazla firmaya devamlı olarak iş güvenliği ve iş sağlığı hizmetleri veriyoruz.</p>
