					<h2>Risk Değerlendirmesi İle İlgili Yanlış Yapılanlar</h2>
					<p>Risk değerlendirmesinin beklenen faydayı sağlaması için uygun ve kaliteli olması gerekir. Risk değerlendirmesi yapan şirketlerin sayısı giderek artmakta iken kalite risk analizi için tartışılan bir kavram olmaya başlamıştır. İşte bu kaliteyi etkileyen ve risk analizi ile ilgili olarak birçok şirketin tekrarladığı sık yapılan yanlışlar şunlardır:</p>
					<ul>
						<li>Risk analizi tek defalık bir iş olarak algılanmaktadır. Oysaki risk analizi düzenli şekilde güncellenmesi gereken bir süreçtir. (İş Sağlığı ve Güvenliği Risk Değerlendirme Yönetmeliği Madde-12)</li>
						<li>Riskler, çalışma koşulları toplu halde analiz edilip değerlendirilmemektedir. Bunun sonucu olarak, birbirini desteklemeyen, kopuk önlemler uygulanmakta, bütüncül bir yaklaşım izlenmediği için etkin sonuç alınamamaktadır. (İş Sağlığı ve Güvenliği Risk Değerlendirme Yönetmeliği Madde-10)</li>
						<li>Risk değerlendirmesi iyi şekilde belgelendirilememektedir. (İş Sağlığı ve Güvenliği Risk Değerlendirme Yönetmeliği Madde-11)</li>
						<li>Risk değerlendirmesi yüzeysel yapılmakta, açıkça görülen ve acil müdahale gerektiren risklere odaklanılmaktadır. Kimyasallardan kaynaklananlar gibi uzun dönemli etkiler, psikososyal riskler, ergonomik faktörler göz ardı edilmektedir.</li>
						<li>Önlemlerin etkinliği yeterince izlenmemektedir. (İş Sağlığı ve Güvenliği Risk Değerlendirme Yönetmeliği Madde-10/3)</li>
						<li>Firma içinde yetkin biri tarafından gerçekleştirilebilecekken firmayı tanımayan kişilerce ve çalışanların katılımı olmaksızın gerçekleştirilmesi istenen sonucu sağlamamaktadır. (İş Sağlığı ve Güvenliği Risk Değerlendirme Yönetmeliği Madde-5 Risk Değerlendirme Ekibi Tarafından Yapılmalıdır.)</li>
					</ul>
					
