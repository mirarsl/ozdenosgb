								<li>Özellikle öksürük ve aksırıktan sonra ellerinizi su ve sabun ile yıkayınız. El hijyeni için alkollü el dezenfektanları da kullanılabilir.</li>
								<li>Ağız, burun ve gözünüze dokunmaktan kaçınınız.</li>
								<li>Eğer grip iseniz diğer insanlara bulaştırmayı önlemek için evinizden çıkmayın, işe gitmeyin. Çocuğunuz grip ise okula göndermeyin.</li>
