					<p>Ülkemizde gıda üretim ve perakende işyerleri başta olmak üzere, çeşitli sıhhi ve gayrisıhhi işyerlerinde çalışanların yaptırmak zorunda oldukları portör muayeneleri, 02 Kasım 2012 tarihli ve 28103 sayılı Resmi Gazetede yayımlanan değişiklik ile kaldırılmıştır. 2013 Temmuz ayı itibariyle gıda işletmelerinde çalışan personelin çalışmadan önce veya hali hazırda çalışıyorlar ise 1 yıl içerisinde hijyen eğitimi alması gerekliliği yasa ile tanımlanmıştır. İlgili yasal düzenlemeye uygun olarak hazırlanan Hijyen Eğitimi Yönetmeliği de 18698 sayılı Resmi Gazete‘de yayımlanarak yürürlüğe girmiştir. Yönetmeliğe göre eğitimler, Milli Eğitim Bakanlığı Hayat Boyu Öğrenme Genel Müdürlüğü tarafından planlanacak ve düzenlenecektir.  </p>
					<h2>Koruyucu Sağlık Muayenesi İhtiyacı</h2>
					<p>Uzmanlık dernekleri yaptıkları açıklamalarla; hijyen eğitiminin olumlu tutum ve davranış değişikliği yaratmak açısından çok önemli olduğunu, ancak çalışanların periyodik olarak sağlıklı olup olmadıklarını değerlendirmeye alternatif bir uygulama olmaması gerektiğini ifade ederek göreve yönelik koruyucu sağlık muayenesi ihtiyacının devam ettiğini belirtmiştir.  </p>
					<h2>İşverenin Sorumluluğu Devam Ediyor</h2>
					<p>Değişiklik yapılan 1593 sayılı umumi Hıfzıssıhha Kanunu incelendiğinde,126ncı madde de yapılan değişiklikle “gıda üretim yerleri ve sıhhi müesseselerde çalışanların her 3 ayda bir muayene olma ve sıhhi rapor alma” zorunluluğunun kaldırıldığı görülmektedir. Ancak yine aynı maddede değişiklikten sonraki halinde yer alan “bulaşıcı bir hastalığı olduğu belgelenenler ile iş yerinin faaliyet ve hizmetlerinden doğrudan yararlananları rahatsız edecek nitelikte hastalığı bulunanlar iyileştiği belgeleninceye kadar belirtilen iş yerlerinde çalışamaz ve çalıştırılamazlar” ifadesi işverenin sorumluluğunun devam ettiğine işaret etmektedir.   </p>
					<p>Ayrıca çalışan sağlığını düzenlemek amacı ile yapılan tüm düzenlemelerin esasını oluşturan 6331 sayılı İş Sağlığı ve Güvenliği Kanunu tüm çalışanlara “portör muayenesi” adı altında olmasa da bir muayenenin mutlaka yapılması gerektiğini söylemektedir. Söz konusu yasal düzenlemeye göre; işveren, çalışanların işle ilgili sağlık ve güvenliğini sağlamakla yükümlü olup çalışana görev verirken, çalışanın sağlık ve güvenlik yönünden işe uygunluğunu göz önüne almalıdır.  Ayrıca işveren; çalışanların sağlık ve güvenliğini etkilediği bilinen veya etkilemesi muhtemel konular hakkında; görevlendirdikleri kişi veya hizmet aldığı kurum ve kuruluşları, başka işyerlerinden çalışmak üzere kendi işyerine gelen çalışanları ve bunların işverenlerini bilgilendirmelidir.  Yine işveren; Çalışanların işyerinde maruz kalacakları sağlık ve güvenlik risklerini dikkate alarak çalışanların, işe girişlerinde, işin devamı süresince belirli aralıklarla sağlık muayenelerinin yapılmasını sağlamak zorundadır.  İş Sağlığı ve Güvenliği Kanunu’nda bulunan bu yükümlülükler de söz konusu işyerlerinde istihdam edilen çalışanların bulaşıcı hastalıklar açısından toplum sağlığına ve kendilerine yönelik bir risk oluşturmadıklarından emin olunması gerektiğini düşündürmektedir.</p>
					<h2>Portör Muayenesi Yükümlülüğü Halen Yürürlükte</h2>
					<p>Tüm bu değerlendirmeler bize göstermektedir ki; Umumi Hıfzıssıhha Kanununun 126nci maddesinde öncesinde ismen belirtilen belirli meslek grupları için yasal bir zorunluluk olan “portör muayenesi” uygulaması kaldırılarak söz konusu personel için “hijyen eğitimi” zorunluluğu getirilmiş olsa da portör muayenesi kapsamında evvelden yapılan sağlık işlemlerinin hijyen eğitimi ile birlikte uygulamaya devam edilmesi hem sağlıklı iş ve çevresi, hem çalışanların olumlu tutum ve davranışları açısından yasal bir sorumluluktur.</p>

