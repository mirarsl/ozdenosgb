					<h2 style="margin-top: 0px; ">Maymun Çiçeği Hastalığı Nedir?</h2>
					
					<p>Maymun çiçeği (Monkeypox) hastalığı, maymun çiçeği virüsü ile enfeksiyonun neden olduğu nadir bir hastalıktır. Hastalığa neden olan virüs çiçek hastalığının etkeni olan variola virüsü ile aynı ailedendir. İlk olarak 1958'de araştırma laboratuvarlarda tutulan maymunlarda ortaya çıkan çiçek benzeri bir hastalık olarak ortaya çıkmıştır. Adında “maymun” geçmekle birlikte maymunlardan daha çok sincap, sıçan, fare gibi kemirgenlerde bulunan ve onlardan insana geçen bir hastalıktır. İlk insan maymun çiçeği vakasının 1970 yılında tanımlanmasından 2022 yılında ortaya çıkan salgına kadar, genelde orta ve batı Afrika ülkelerine seyahat veya hasta hayvanlarla temasla bağlantılı olarak tanımlanmıştır. <sup><a href="#fn1" id="ref1">1</a></sup> Tüm dünyada 15 Haziran 2022 itibari ile, 2103 laboratuvar onaylı vaka ve bir ölüm bildirilmiştir. Doğrulanmış vakaların çoğunluğu Avrupa'dadır. <sup><a href="#fn2" id="ref2">2</a></sup> </p>
					<p>Virüs insanlara çoğunlukla kemirgenler ve primatlar gibi vahşi hayvanlardan bulaşmaktadır. Yetersiz pişmiş et ve enfekte hayvanların diğer ürünlerini yemek de olası bir risk faktörüdür. İnsandan insan bulaşta; lezyonlar, vücut sıvıları, solunum damlacıkları ve yatak örtüsü gibi kontamine materyallerle temas suçlanmaktadır.<sup><a href="#fn3" id="ref3">3</a></sup> İnsandan insana bulaşmanın esas olarak büyük solunum salgısı damlacıkları ile olduğu düşünülmektedir. Büyük damlacıklar uzak mesafelere gidemediğinden insandan insana bulaşma için; yüz yüze, uzun süreli ve yakın temas gereklidir. Bu da COVID-19’a benzer büyük salgınlar yapmasını engelleyebilecek bir özelliktir. Virus enfekte insanın vücut sıvılarına, cilt lezyonlarına doğrudan temas ile direkt olarak veya bunlarla kirlenmiş cansız maddelerle temas ile dolaylı olarak bulaşabilir. <sup><a href="#fn4" id="ref4">4</a></sup> Hastaların büyük bir kısmının erkek olması dikkat çekicidir. Hastaların kuluçka döneminde cinsel ilişki hikayesinden bahsetmesi cinsel ilişki ile hastalık arasında bir bağlantının göz önünde bulundurulması gerektiğini düşündürmektedir. <sup><a href="#fn5" id="ref5">5</a></sup></p>
					

					<h2>Çiçek Hastalığı Aşısı, Maymun Çiçeği'a Karşı Etkili midir?</h2>
					<p>Çiçek hastalığı ile aynı aileden olması nedeniyle 1970li yılların sonuna kadar tüm dünyada uygulanmış olan çiçek aşının maymun çiçeğine karşı koruyucu olduğunu söyleyen yayınlar bulunmaktadır.<sup><a href="#fn6" id="ref6">6</a></sup> Aşılamanın uzun yıllar önce sonlanmış olması nedeniyle koruyuculuğun azalmış olmasından bahsedilebilir. Bununla birlikte hastalığın yaygın olarak görüldüğü Afrika’daki ev içi bulaşmaların çiçek aşısı olmuş kişilerde daha az olduğu ve ağır hastalıktan korundukları gözlenmiştir. <sup><a href="#fn7" id="ref7">7</a></sup> Ancak bu çalışmanın aksine, söz konusu aşılamanın bu hastalık için koruyucu olmadığı durumlara işaret eden yayınlar da bulunmaktadır. <sup><a href="#fn8" id="ref8">8</a></sup></p>
					

					<h2>Maymun Çiçeği Hastalığının Belirtileri Nelerdir?</h2>
					<p>Hastalık; ateş, baş ağrısı, yorgunluk, yaygın vücut ağrıları, lenf bezlerinde şişlik ve ciltte döküntülere neden olmaktadır. Bulgular, virüs ile temas ettikten ortalama 6-13 gün sonra ortaya çıkar. Ciltteki döküntüler, daha çok yüzde kollarda ve bacaklarda, kızarıklık şeklinde başlayıp ardından içleri önce berrak sonra sarımsı renkte bir sıvıyla dolu kabarıklıklar şeklinde görülmektedir. Döküntüler bir süre sonra kabuklanarak genellikle 2-4 hafta içerisinde kendiliğinden iyileşmektedir. Bağışıklık sistemi baskılanmış kişilerde hastalık ağır seyredebilir. <sup><a href="#fn9" id="ref9">9</a></sup></p>
					<p>Nadir görülen bir hastalık olması ve yakın temas olmadan insanlar arasında kolayca yayılmaması nedeniyle yaygın bir salgın riskinin düşük olduğu değerlendirilmektedir. <sup><a href="#fn10" id="ref10">10</a></sup></p>
					
					<h3>Virüs'ten Korunmak İçin</h3>
					<p>Kamu sağlık otoriteleri herkes için; virüsü barındırabilecek hayvanlarla, hasta hayvanların ürünleriyle veya kontamine malzemeyle temastan temastan kaçınılmasını (maymun çiçeğinin meydana geldiği bölgelerde hasta olan veya ölü bulunan hayvanlar dahil), hastaları ve enfeksiyon riski altında olabilecekleri izole etmeyi, hasta hayvanlar veya insanlarla temastan sonra iyi el hijyeni uygulanmasını, hastalarla temas ederken kişisel koruyucu ekipman (KKD) kullanımını, seyahat dönüşü şikayetlerin ortaya çıkması halinde bir sağlık kuruluşuna başvurmayı önermektedir. <sup><a href="#fn11" id="ref11">11</a></sup></p>
					<p>Çalışan sağlığı risklerini belirlemek amacıyla geçmişte yapılan çalışmalar hasta hayvanlarla temasın önemine dikkat çekmektedir. <sup><a href="#fn12" id="ref12">12</a></sup> Söz konusu çalışmada; alerji ilacı kullanan çalışanların, alerjik şikayetleri ile birlikte bulunan hastalıkları nedeniyle sık ağız ve burunlarına dokunma alışkanlıklarının hastalığın bulaşmasını arttırabileceğinden özellikle bahsedilmiş olması ilgi çekicidir. Bu çalışma; veteriner kliniklerinde, evcil hayvan mağazalarında ve hayvan üretimi, dağıtımı ve hayvansal ürünlerin işlendiği iş kollarında, özellikle canlı olmasa dahi ithal kemirgenlerle temas mevcudiyetinde maymun çiçeği hastalığı riskinin göz önünde bulundurulması gerektiğini düşündürmektedir.</p>
					<p>Birleşik Krallık’da yakın zamanda yayınlanan hastalıkla ilgili bir rapor, sauna benzeri alanlarda bulunmanın ve seks işletmelerinin bulaşa etkisi olabileceğine işaret etmektedir. <sup><a href="#fn13" id="ref13">13</a></sup> Söz konusu alanlarda bulunmanın çalışan sağlığı risklerini ve bulaşı engellemek için dezenfeksiyon ile genel hijyen tedbirlerinin gözden geçirilmesinin önemini ortaya koyması açısından önemlidir.</p>
					<p>Hastalığın yakın temas, vücut sıvıları ve iri solunum damlacıkları ile bulaş riski nedeni ile bir diğer mesleki risk sağlık çalışanları için mevcuttur. Özellikle yeterli kişisel koruyucu donanım kullanmadan hasta bakımı sağlayan doktorlar, hemşireler ve diğer sağlık çalışanları ile kirlenmiş yüzeyleri, malzemeleri temizleyen ve dezenfekte eden hastane personelinin risk altında olduğu unutulmamalıdır. <sup><a href="#fn12" id="ref12">12</a></sup></p>
					
					<h3>Bir Biyolojik Risk Faktörü Olarak Maymun Çiçeği Virüsü</h3>
					<p>Tüm bu bilgiler bizlere; maymun çiçeği (monkeypox) hastalığının tüm dünyayı etkisi altına alacak bir salgın yapmasının beklenmediğini ancak 1970 yılında insanlar için bir tehdit olmasından sonra özellikle riskli ülkelerde ve risk grubundaki iş kollarında dikkatle değerlendirilmesi gereken bir biyolojik risk faktörü olduğunu düşündürmektedir.</p>

