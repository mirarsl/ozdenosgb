					<ul>
						<li>Oruç tutarken yeterli ve dengeli beslenmeye özen gösterin.</li>
						<li>Tek seferde büyük porsiyonlar yerine, iftardan sonra aralıklı ve her seferinde küçük porsiyonlar şeklinde beslenin.</li>
						<li>Yemeklerinizi hızlı yemekten kaçının, yavaş yavaş ve iyice çiğneyerek tüketin.</li>
						<li>Sahur öğününüzü atlamayın, mutlaka yapın.</li>
						<li>Sahurda süt, yoğurt, peynir, yumurta, tam tahıllı ekmekler gibi yiyeceklerden oluşan hafif bir kahvaltı yapabilir ya da çorba, zeytinyağlı yemekler, yoğurt ve salatadan oluşan bir öğün tercih edebilirsiniz.</li>
						<li>İftara peynir, domates, zeytin gibi kahvaltılıklar veya çorba gibi hafif yemeklerle başlayıp 10-15 dakika sonra az yağlı et yemeği, sebze yemeği veya salatayla devam edebilirsiniz. Hem enerji veren hem de kan şekerini hızlı bir şekilde yükselten besinler olan; beyaz ekmek, pirinç pilavı, kızarmış patates gibi yiyecekler yerine bulgur pilavı, kepekli ekmek veya kepekli makarna vb. tercih edin.</li>
						<li>Mümkün olduğunca iftar ve sahurda badem, ceviz vb tüketmeye özen gösterin.</li>
						<li>İftarda aşırı şerbetli, yağlı tatlılar yerine; sütlü tatlılar (sütlaç, güllaç, muhallebi vb.) veya meyve tatlıları tercih edin.</li>
						<li>Susama hissi duymasanız bile iftar ve sahur arasında sık sık su için. Suya ek olarak kafein içeren içecekler yerine de süt, ayran, sade soda, taze sıkılmış meyve-sebze suları, ıhlamur ve kuşburnu gibi bitki çayları tercih edebilirsiniz.</li>
