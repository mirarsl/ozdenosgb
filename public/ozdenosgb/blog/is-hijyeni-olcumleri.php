						<li><b>El-kol titreşimi:</b> İnsanda el kol sistemine aktarıldığında, çalışanın sağlık ve güvenliği için risk oluşturan ve özellikle de damar, kemik, eklem, sinir ve kas bozukluklarına yol açan mekanik titreşimi ifade eder.</li>
