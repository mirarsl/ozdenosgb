					<p>Uyku fizyolojik bir ihtiyaçtır. Ancak stres ile ilişkisi söz konusu olduğu zaman, beslenmeye benzer bir şekilde hem bir stres nedeni hem de bir stres yönetim aracı olarak karşımıza çıkmaktadır.</p>
					<p>Uyku sırasında salgılanan melatonin hormonu benzersiz etkileri ile psikolojik ve metabolik olarak vazgeçilmez role sahiptir. Ayrıca sirkadiyen ritm denilen bir zamanlamaya bağlı olarak vücudumuzdaki tüm hormonların öncülü olduğu söylenebilecek kortizon metabolizması uyku sırasında yenilenmektedir. Uykunun metabolik, psikolojik hatta mental etkileri hakkında daha fazlası için, Matt Walker’in “Sleep is your superpower “ isimli TED konuşmasını dinleyebilirsiniz. <sup><a href="#fn1" id="ref1">[1]</a></sup></p>
					<p>Uyku ile ilgili tartışmalar nerede ise her zaman; kaç saat ve hangi saatler arasında uyunması gerektiği sorularıyla başlamaktadır. Yaşa ve kişiye bağlı olarak uyku ihtiyacının değişeceği bilinmesine rağmen uzmanlar günlük ortalama 7 saat uykuya ihtiyaç duyduğumuzu ifade etmektedir. Sirkadiyen ritm olarak adlandırılan biyolojik saat göz önünde bulundurulduğunda saat 23 ile 05 arasındaki uykunun öneminin yüksek olduğu görülmektedir.</p>
					<p>Ancak uyku kalitesinin kimi durumlarda uyku süresinden daha önemli olabileceği unutulmamalıdır. Uyku kalitesini arttırmak için öneriler 11 başlık altında toplanabilir.</p>
					<h2>1.      Uyku rutininiz olsun.</h2>
					<p>Aynı saatte yatıp aynı saatte kalkın. Saat 23 ile 05 arasındaki uyku kıymetlidir. Özel nedenlerle yatağa gitmeniz gecikirse mümkünse kalkma saatinizi değiştirmemeye ama bir sonraki gün eksiğinizi tamamlamaya çalışın. Uykuya borcunuzu şu ya da bu şekilde ödemelisiniz.</p>
					<h2>2.      Uykuya hazırlık ritüeliniz olsun.</h2>
					<p>Her seferinde uykuya aynı şekilde hazırlanmak uykuya dalmanızı kolaylaştıracaktır. Uykuya hazırlanırken duş alabilirsiniz, kitap okuyabilirsiniz, müzik dinleyebilirsiniz, nefes egzersizleri gibi rahatlatıcı aktiviteler uygulayabilirsiniz. Elbette müzik ve kitap tercihinizin sakinleştirici olması önemlidir.</p>
					<h2>23.      Dijital detoks faydalıdır.</h2>
					<p>Uykudan önce ve uyku sırasında dijital cihazları kullanmayın, mecbursanız uçuş veya uyku modunda kullanın. Kullandığınız cihazların ekranı parlaklığını mavi ışığı azaltacak şekilde düzenleyebilirsiniz. Uyku sırasında kablosuz ağ kullanımı, şarj etme vb elektromanyetik yükü artırabilecek işlemleri yapmamaya çalışın.</p>
					<h3>4.      Rahat bir ortamda uyuyun.</h3>
					<p>Yatak konforunuz önemlidir. Nasıl yatakların size konforlu geldiğine siz karar vermelisiniz. Rahatsız olduğunuz yataklarda uyumak için ısrarcı olmayın.</p>
					<p>Termal konfor kişiden kişiye değişebilir. Ancak sıcağın da soğuğun da uyku kalitesini azaltacağını; uyku sırasında metabolizmanız yavaşlayacağından mevsime uygun bir örtü ve rahat kıyafetler ile pasif ısınma ihtiyacınızı karşılamanız gerektiğini unutmayın.  </p>
					<p>Termal konfor ve uyku kalitesi hakkında daha fazla bilgi için Li Lan ve Zhiwei Lian’ın “Ten questions concerning thermal environment and sleep quality” çalışmasını inceleyebilirsiniz.<sup><a href="#fn2" id="ref2">[2]</a></sup></p>
					<h3>5.      Gün içerisindeki fiziksel aktivite faydalıdır.</h3>
					<p>Fiziksel olarak aktif bir yaşamın ve egzersiz yapmanın birden fazla mekanizma ile uyku kalitesini artırdığını biliyoruz. Ancak yıkıcı olmayan egzersizlerin tercih edilmesi ve uygun donanım kullanılması dikkat edilmesi gereken noktalar olarak karşımıza çıkıyor.</p>
					<h3>6.      Karanlıkta uyuyun.</h3>
					<p>Gerek melatonin ve kortizon metabolizması gerekse uyku kalitesi açısından karanlık önemlidir. Karanlıkta uyumanın sağlanamadığı coğrafi koşullarda ve mesleklerde artmış hastalıklar ve kazalar bu konunun önemini açıkça göstermektedir.<sup><a href="#fn3" id="ref3">[3]</a></sup></p>
					<h4>7.      Örtünmenin rahatlatıcı etkisini kullanın.</h4>
					<p>Yapılan bir çalışma; battaniye, yorgan vb. örtü kullanmanın uyku kalitesini arttırdığını göstermiştir. Araştırmacılar kullanılan örtünün ağırlığının olumlu etkisinden de bahsetmişler. Uykusuzluk çekiyorsanız deneyebilirsiniz. <sup><a href="#fn3" id="ref3">[4]</a></sup></p>
					<h4>8.      Uykuya dalmak için sizi iyi hissettirecek duygu ve düşüncelerinizi kullanabilirsiniz.</h4>
					<p>Uykuya dalmakta zorlanan kişiler çeşitli yöntemlerle bunu aşmaya çalışmaktadır. Uykuya hazırlık rutini ve doğru besin tercihinin uykuya dalmada olumlu etkisi olduğunu biliyoruz. Bunlara ilave olarak uykuya dalarken kendinizi iyi hissetmenizi sağlayacak anılarınızı, duygularınızı ve düşüncelerinizi kullanabilirsiniz.</p>
					<h4>9.      Uyanacağınız saati doğru planlayın.</h4>
					<p>Uyku evrelerden oluşur. Bu evreler sırasında uyku gittikçe derinleşmekte ve sonrasında hafiflemektedir. Uykunun hafiflediği dönemde uyanmak dinç uyanmanızı sağlayacaktır. Kişiden kişiye değişmekle birlikte 90 dakikalık dönemlerle uykunuzun hafiflediğini düşünebilirsiniz. Basit uyku takip programları kullanarak kişisel uyku analizinizi yapabilir ve sizin için doğru uyanma süresini belirlemeye çalışabilirsiniz.</p>
					<h4>10.  Doğru beslenin.</h4>
					<p>Özellikle uykudan önceki öğüne, uyku kalitesi ile doğrudan ilişkili olduğu için özen gösterilmelidir. Uyku sırasında kan şekerinin dalgalanmasına neden olacak karbonhidrattan zengin öğünler yerine, lif zengini sağlıklı yağ ve protein içeren hafif ama uzun süre tok tutacak besinler tercih edilmelidir.</p>
					<p>Uykuya dalmakta zorluk yaşayanlar uykudan önce rahatlatıcı bitki çayları tercih edebilir. Uykuya yakın çay, kahve ve diğer uyarıcıların tüketilmesi uykuya dalmayı zorlaştıracaktır. İdrar söktürücü olduğu bilinen besinleri (kavun, karpuz vb) uykuya yakın tüketmek uyku kalitesini bozacaktır.</p>
					<p>Akşam yemeğinin saati ile gün batımı ve uyku arasındaki süreyi, Covid-19 ilişkili ölüm oranı ile karşılaştıran bir çalışma, uyku ve beslenme arasındaki ilişkinin sonuçları için düşündürücüdür.<sup><a href="#fn5" id="ref5">[5]</a></sup></p>
					<h4>11.  Gündüz uykusu faydalı olabilir.</h4>
					<p>Yapılan çalışmalar, son yıllarda sıkça konuşulan "Power Nap" ya da "şekerleme" olarak adlandırılan kısa uykuların işe yaradığını göstermektedir.<sup><a href="#fn6" id="ref6">[6]</a></sup> Sıra dışı bir nedenle uyku rutiniz bozulduğunda veya düzenli olarak, gün içerisinde şekerlemeler yaparak stresinizi yönetebilir, yorgunluk ve dikkatinizi arttırabilirsiniz.</p>

