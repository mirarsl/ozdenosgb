					<h2>İç ortam kalitesini artırmak için şu öneriler öne çıkmaktadır.</h2>
					<p><b>Çalışma ve yaşama ortamınızı havalandırın.</b> Özellikle sabahları ve uzun çalışma sürelerinden sonra havalandırma önemlidir. Mümkünse pencere benzeri doğrudan temiz hava kaynakları kullanılması önerilmektedir. Kapalı devre havalandırma sistemi veya klima kullanılıyorsa filtrelerin düzenli olarak temizlendiği ve zaman zaman temiz hava ile desteklendiğinden emin olunmalıdır.</p>
