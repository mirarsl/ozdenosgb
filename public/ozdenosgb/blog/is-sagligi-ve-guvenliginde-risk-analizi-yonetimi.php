					<h2>Risk Analizi Nasıl Yapılır?</h2>
					<p>Risk değerlendirmesinde, riskleri tespit etmek için izlenebilecek birçok yol vardır. İşletmenin yapısı, süreçlerin özellikleri, gerçekleştirilen görevler ve kullanılan teknolojiye göre mevcut birçok yöntemden biri seçilebilir. Bu yönde herhangi bir zorlama bulunmamaktadır.</p>
					<p>Birçok işletme için, 5-adım yaklaşımı hem kolay anlaşılır hem de ihtiyaç duyulan yönlendirmeyi sağlamaktadır.</p>
					<h3>1-) Tehlikelerin Belirlenmesi</h3>
					<p>Bu adımda çalışanlara, üretime ve işletmeye nelerin zarar verebileceğini belirlemeniz gerekir.</p>
					<h3>2-) Tehlikelerin Değerlendirilmesi</h3>
					<p>Birinci adımda oluşturduğunuz tehlikeler listesinin değerlendirilmesi ile hangileri için ne tür önlemler alınacağının ve hangileri için risk derecelendirilmesi yapılması gerektiğine karar verilir.</p>
					<h3>3-) Risklerin Derecelendirilmesi</h3>
					<p>İkinci adımda, risk derecelendirilmesi yapılmasına karar verilen tehlikelerin her biri için ayrı ayrı risklerin ağırlık oranları hesaplanarak derecelendirme yapılır ve riskler öncelik sıralamasına tabi tutulur.</p>
					<h3>4-) Kontrol Önlemlerinin Uygulanması</h3>
					<p>İkinci ve üçüncü adımlarda alınmasına karar verilen önlemlerden hemen ortadan kaldırılabilecek tehlikeler için gerekli önlemler alınır ve tekrar ortaya çıkmamaları için uygun bir kontrol periyodu belirlenir. Belirli bir maliyet ve zaman gerektiren ve acil olmayan önlemler için uygulama planları yapılarak uygulamaya konulur.</p>
					<h3>5-) Denetim, İzleme ve Gözden Geçirme</h3>
					<p>İşyerlerinde gerçekleştirilen risk yönetiminin tüm aşamaları ve uygulaması düzenli olarak denetlenir, izlenir ve aksayan yönler yeniden gözden geçirilir.</p>
	
