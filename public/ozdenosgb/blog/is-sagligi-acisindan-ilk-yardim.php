					<h2 class="first-header">Modern İş Sağlığı Bakış Açısı</h2>
					<p>Çalışanların yaptıkları iş ile sağlık durumları arasındaki ilişki çok uzun süreden beri bilinse de başlangıçta üretim güvenliğini odağına alan iş güvenliği bakış açısının çalışan güvenliğini önceleyen modern haline dönüşmesi nispeten yenidir. Modern iş sağlığı bakış açısı, iş yeri ve işin yürütüm şartları ile ilişkilendirilebilecek sağlık sorunlarını ortadan kaldırmayı hedeflemektedir. </p>
					<p>Söz konusu sağlık sorunları kimi zaman uzun süren etkilenim ile ortaya çıkan iş ile ilişkili hastalıklar kimi zaman da kazalar olarak kendisini göstermektedir. Her iki durumda da istenmeyen sağlık sonuçlarını engelleyecek çabalar kadar istenmeyen durumlar gerçekleştikten sonra yapılması gerekenler hakkında yeterince donanımlı personel istihdamı oldukça önemlidir.  </p>
					
					<h2>İlk Yardım Bilinci: Bir Toplum Sağlığı Sorumluluğu</h2>
					<p>Herhangi bir kaza ya da yaşamı tehlikeye düşüren bir durumda, sağlık görevlilerinin tıbbi yardımı sağlanıncaya kadar, hayatın kurtarılması ya da durumun daha kötüye gitmesini önleyebilmek amacıyla olay yerinde, tıbbi araç gereç aranmaksızın mevcut araç ve gereçlerle yapılan ilaçsız uygulamalar “ilk yardım” olarak adlandırılmaktadır.  İlk yardım uygulamaları hakkında her çalışanın yeterince bilgi sahibi olması temelde bir toplum sağlığı sorumluluğudur. Ayrıca 6331 sayılı İş Sağlığı ve Güvenliği Yasasına göre çalışan sayısı ve işyeri tehlike sınıfı göz önünde bulundurularak belirli sayıda sertifikalı ilk yardımcı bulundurulması bir zorunluluktur. İlk yardım yönetmeliğinde çok tehlikeli sınıfta bulunan iş yerlerinde 10 kişiden 1 kişinin, tehlikeli sınıfta bulunan iş yerlerinde 15 kişiden 1 kişinin, az tehlikeli sınıfta bulunan iş yerlerinde 20 kişiden 1 kişinin sertifikalı ilk yardım uygulayıcısı olarak belirlenmesi ve gerekli eğitimi alması istenmektedir.</p>
					<p>Sertifikalı ilk yardım uygulayıcısı belirlenirken yeterli sayıda personel istihdamı kadar söz konusu personelin iş yerinin doğru birimlerinden seçimi de önemlidir. İş kazalarının gerçekleşme şanssızlığının daha yüksek olduğu iş yeri bölümlerinde vardiyalı çalışma şartları da göz önünde bulundurularak yeterince ilk yardımcı personel planlanmalıdır. </p>
					<p>Planlamada göz önünde bulundurulması gereken bir diğer konu işin yürütüm şartları olarak karşımıza çıkmaktadır. Risk analizi göz önünde bulundurularak belirlenen riskler ilk yardımcı personelin eğitimlerinde dikkate alınmalıdır. Göze yabancı cisim kaçma şanssızlığının bulunduğu bir iş yerinde göz yıkama sıvılarının hangi durumlarda ve nasıl kullanılması gerektiğini veya sonrasında sağlık tesisine kazazedenin nasıl yönlendirileceğini önceden belirleyerek verilecek eğitimlerde detaylandırmak bu bakış açısı ile dikkate değer bir örnektir.  </p>
					
					<h2>İlk Yardım Eğitimi - İşyeri Riskleri Uyumu</h2>
					<p>Eğitimlerin belirli aralıklarla tekrarlanmasının yasal bir zorunluluk olduğu kadar ilk yardımcı personelin etkinliğine olumlu katkısı nedeniyle önemlidir. Eğitimlerin iş yerinde yaşanması muhtemel kazaları konu alacak senaryolar şekilde düzenlenmesi veya zaman zaman yapılacak tatbikatlarda iş yeri risklerini göz önünde bulundurarak hazırlanan acil durum senaryolarında ilk yardım sorumlulukları eklenmesi faydalı olacaktır. </p>
					<p>İlk yardım uygulayıcılarının ihtiyaç duyacağı malzemeler de işyeri ve işin yürütüm şartları ile yasal sorumluluklar göz önünde bulundurularak belirlenmelidir. Otomatik defibrilatör cihazlarının kullanımı artık ilk yardım eğitiminin bir parçasıdır ve özellikle kalabalık, sağlık tesisine yakın olmayan iş yerlerinde bulundurulması faydalı olacaktır.</p>
					
					<h3>Salgın Döneminde İlk Yardım</h3>
					<p>Salgın döneminde ilk yardım uygulamalarında bulaşı dikkate alan kaygılar ortaya çıkmıştır. Çalışanların ilk yardım uygulamalarına katılımını artırmak için bu ve benzeri hassasiyetler dikkate alınmalıdır. Bazı müdahalelerin (suni solunum, açık yaralara ve kanamalara müdahale vb.) HIV, tüberküloz, hepatit B veya SARS bulaşma riski nedeniyle koruyucu donanımla uygulanması Covid-19 salgınından önce önerilmiş olsa da salgın sonrası bu konu daha önemli hale gelmiştir. Rehberler arasında bazı farklılıklar olsa da temel olarak hepsi; gereksiz temas ve yakınlaşmadan kaçınılmasını, KKD (kişisel koruyucu donanım) kullanımını önermektedir.  Suni solunum ihtiyacı uygun maliyetli tek kullanımlık Ambu® setleri veya benzeri ürünlerle de karşılanabilir.  </p>
					<p>Sonuç olarak; ilk yardım uygulamaları iş sağlığı ve güvenliğinin ayrılmaz bir parçası olarak değerlendirilmelidir. Yapılan işin ve iş yerinin riskleri göz önünde bulundurularak nitelik ve nicelik açısından yeterince personelin eğitilmesi esastır. Söz konusu eğitimler sık karşılaşılabilecek durumlar göz önünde bulundurularak hazırlanmış senaryolarla verilmeli ve salgın benzeri değişen durumlar değerlendirilerek güncellenmelidir.</p>



