					<h2>İşverenlere iş güvenliği tedbirlerine uyulup uyulmadığını izleme, denetleme ve gereğini yapma yükümlüğü getirmiştir.</h2>

					<p>İşverenler bu yükümlülüklerini yerine getirebilmek için iş güvenliği sorumlusu görevlendirmelidir. Ayrıca, Binaların Yangından Korunması Hakkında Yönetmeliğin 124. Maddesi, binada en yetkili amirin yangın amiri olduğunu belirtmektedir. 125. Maddesi ise mesai saatleri içinde bu görevini bir başkasına devredebileceğinden bahsetmektedir. Bu nedenle ayrıca yangın sorumlusu da görevlendirilmelidir. Bu işleri Yangın ve İş Güvenliği Sorumlusu atayarak bir kişinin de görevlendirilmesi sağlanabilir.</p>

 

					<h2>İşverenleri risk değerlendirmesi yapma/yaptırma yükümlülüğü getirmiştir.</h2>

					<p>Çalışan sayısına bakılmaksızın her işveren, İşveren vekili, iş güvenliği uzmanı, işyeri hekimi, çalışan temsilcisi ve destek elemandan ekip oluşturmalı ve risk değerlendirmesi yaptırmalıdır. (İşverenin iş güvenliği uzmanı ve işyeri hekimi bulundurma yükümlülüğü yok ise bu ekipte bulunma şartı aranmıyor)</p>

					<p>İşverenler çalışanlarına görev verirken çalışanın sağlık ve güvenlik yönünden uygunluğunu göz önünde bulundurmalıdır. İşverenler, hayati ve yakın tehlike bulunan yerlere görevliler haricindeki kişilerin ulaşmasını engellemelidir.</p>

 

					<h3>Sınıfına göre iş güvenliği uzmanı, işyeri hekimi ve sağlık elemanı görevlendirme yükümlülüğü getirmiştir.</h3>

					<p>Bu görevlendirmeleri kendi içinden görevlendirebileceği gibi, ortak sağlık ve güvenlik biriminden de bu hizmetleri alabilir.</p>
					<ul>
						<li>Görevlendirilen kişilerin hizmetlerinde ihtiyaç olan araç, gereç, mekân ve zaman gibi gerekli bütün ihtiyaçlarını karşılamak,</li>
						<li>Görevlendirilmiş kişiler arasında iş birliği ve koordinasyonu sağlamak,</li>
						<li>İş sağlığı ve güvenliği ile ilgili mevzuata uygun olan ve yazılı olarak bildirilen tedbirleri yerine getirmek,</li>
					</ul>
					<p>Gibi işverenlerin yükümlülükleri bulunmaktadır.</p>

 

					<p>Bu görevlerini de çalışan sayısına gore kademeli olarak yerine getirme yükümlülüğü bulunmaktadır</p>

					<p>50’den fazla çalışanı olan tüm firmaların yükümlülükleri bulunmaktadır. 50’den az çalışanı olan tehlikeli ve çok tehlikeli işlerde çalışanlara 30.06.2013 tarihinden, 50’den az çalışanı olan ve tüm kamu kurumlarında ise 30.06.2014 tarihinden itibaren yükümlü kılmaktadır.</p>

 

					<h3>Acil Durum Planı ve Yangınla Mücadele Yükümlülükleri.</h3>

					<p>Meydana gelebilecek acil durumları önceden değerlendirerek, çalışanları ve çalışma çevresini etkilemesi mümkün ve muhtemel acil durumları belirleyerek ölçüm ve değerlendirmeleri yapıp,  yazılı doküman olarak hazırlamakla ve bunların olumsuz etkilerini önleyici ve sınırlandırıcı tedbirleri almakla yükümlü bulunmaktadır.</p>

					<p>Acil durum ekipleri oluşturmalı (Söndürme, kurtarma, koruma ve ilkyardım) ve İstanbul İtfaiyesi Eğitim Dairesi Başkanlığından eğitim almaları sağlanmalıdır.</p>

					<p>Acil durum planı hakkında tüm çalışanlarının bilgilenmelerini sağlamalı, her altı ayda bir (İSG Tüzüğüne göre) tatbikat yapılması sağlanmalıdır.</p>

 

					<h2>İş kazası ve meslek hastalıkları ile ile bildirim yapmak</h2>

					<p>İşyerlerinde kişinin zarar görmesi (iş kazası yada meslek hastalığı),İşyeri, makine yada iş ekipmanı zarar görürse/zarar görme potansiyeli var ise, iş güvenliği uzmanı tarafından detaylı rapor hazırlanır, bird aha yaşanmaması için ne gibi tedbirlerin gerekliliği belirtilir. İş Sağlığı ve Güvenliği Kurulu var ise Kurula, yok ise doğrudan işverene Rapor sunulur.</p>

					<p>İSG Kurulunun olduğu durumda, İSG Kurulunda değerlendirilerek işverene bir daha olmaması için tavsiyede bulunanrak bir Rapor sunar.</p>

					<p>Şayet çalışan zarar görmüş ise, derhal kolluk kuvvetine (polis yada jandarma) ve kazadan sonraki üç iş günü içinde (meslek hastalığını öğrendiği tarihten itibaren üç iş günü içinde) Sosyal Güvenlik Kurumuna online olarak müracaatta bulunur.</p>

 

					<h3>İşverenler, işe girişte ve düzenli aralıklarla sağlık raporu düzenleme yükümlülüğü bulunmaktadır.</h3>

					<p>İşe girişte, iş değişikliğinde, iş kazası yada meslek hastalığı nedeniyle 15 günden fazla işten uzak kalmalarında ve düzenli aralıklarla işyeri hekimince sağlık gözetimine tabi tutma yükümlülüğü bulunmaktadır. Bu sağlık gözetiminden doğan kaynaklı her türlü ek maliyeti işverence karşılanma yükümlülüğü getirmiştir.</p>

					<p>Tehlikeli ve çok tehlikeli sınıfta yer alan işyerlerinde çalışacaklar, yapacakları işe uygun olduklarını belirten sağlık raporu olmadan işe başlatılmalarını yasa yasaklamıştır.</p>

 

					<h3>İşverenler, çalışanlarını eğitmek zorunda bulunmaktadır.</h3>

					<p>Yasa, üç eğitimden bahsetmektedir.</p>
					<ul>
						<li><b>Yeni eğitim:</b> İşe girişte; Yapacağı işin riskleri, acil kaçış yolları ve toplanma alanı, yangın söndürme cihazlarının kullanımı gibi konularda bilgilendirilmelidir. Genellikle oryantasyon yada iş başı eğitimi olarak adlandırılır. Ancak yasa yeni eğitim olarak adlandırmaktadır.</li>
						<li><b>İlave eğitim:</b> Yıllık eğitim planına gore yıl içerisinde düzenlenen eğitimde; Çalışanların İş Sağlığı ve Güvenliği Eğitimlerinin Usul ve Esasları Hakkında Yönetmelik 11Maddesinde belirtilen yada gibi konularda verilen eğitimlerdir. Genellikle temel iş sağlığı ve güvenliği eğitimi olarak adlandırılır. Ancak yasa ilave eğitim olarak adlandırılmaktadır.</li>
						<li><b>İleri eğitim:</b> Yine eğitim planına gore yıl içerisinde düzenlenen eğitimlerden olan ; çalışanın yapacağı işin özelliklerine gore verilen eğitimlerdir. Yüksekte çalışmak zorunda kalanlara “yüksekte güvenli çalışma”, kimyasallarla çalışanlara “kimyasallarla güvenli çalışma”, elektirkle çalışanlara “elektrikle güvenli çalışma” gibi düzenlenen eğitimlerdir.</li>
					</ul>
					
					<p>Mesleki eğitim alma zorunluluğu bulunan tehlikeli ve çok tehlikeli sınıfta yer alan işlerde, yapacağı işle ilgili mesleki eğitim belgesi olmayan kimse çalıştırılmamalıdır.</p>

					<p>İş kazası yada meslek hastalığı nedeniyle işten ayrılmak zorunda kalan çalışanlar, işe dönüşlerde ilave eğitim almadan işbaşı yaptırılmamalıdır.</p>

					<p>Altı aydan fazla işten ayrı kalanlara, işe başlarken yenileme eğitimi verilmelidir.</p>

					<p>Tehlikeli ve çok tehlikeli sınıfta yer alan işyerlerinde; iş sağlığı ve güvenliği eğitim belgesi olmayanlar çalıştırılmamalıdır.</p>

					<p>Eğitimlerin maliyet, hiçbir şekilde çalışanlara yansıtılmamalıdır.</p>

 

