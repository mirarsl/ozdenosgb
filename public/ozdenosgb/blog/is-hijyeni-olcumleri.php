					<h4>Gürültü Ölçümü</h4>
					<p>Gürültü, canlılar üzerinde olumsuz etki yapan ve rahatsız eden düzensiz titreşimlerin meydana getirdiği yüksek seslere denir.</p>
					<p>Gürültü ölçümü, çalışma alanında yapılan incelemeler sonrasında belirlenen ölçüm stratejileri ile gerçekleştirilir.</p>
					<ol>
						<li><b>Görev Tabanlı Ölçüm:</b> Yapılan işin iyi belirlenmiş alt görevlere bölünebildiği, her bir görevin çalışma süresinin kesin olarak belirlenebildiği, ses seviyesinde az miktarda değişim gözlemlendiği (kararlı gürültü) durumlarda kullanılır.</li>
						<li><b>İş Tabanlı Ölçüm:</b> Yapılan alt görevlerin tam olarak belirlenemediği, detaylı iş analizinin mümkün olmadığı durumlarda İş Tabanlı Ölçüm Stratejisi kullanılır.</li>
						<li><b>Tam Gün Ölçüm Stratejisi:</b> Yapılan işlerin ve çalışma sürelerinin tanımlanmasının zor olduğu, belirlenemediği, çalışanların gürültü maruziyetinin karmaşık olduğu, tahmin edilemediği durumlarda kullanılır.</li>
					</ol>
					
