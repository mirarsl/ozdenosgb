					<p><a href="http://www.ozdenosgb.com/is-sagligi-ve-guvenligi"><b>İş Sağlığı ve Güvenliği</b></a> kavramı, İşçi Sağlığı ve İş Güvenliği kavramından farklı olarak, tehlikelerin önlenmesinin yanında risklerin öngörülmesi, değerlendirilmesi ve bu riskleri tamamen ortadan kaldırabilmek ya da zararlarını en aza indirebilmek için yapılacak çalışmaları da içermektedir. Evrensel anlamda İş Sağlığı ve Güvenliği; henüz bir tehlike oluşmamış, işletmede bir arıza oluşmamışken bile işletmede oluşabilecek tehlikelerin ve risklerin öngörülerek bunların kabul edilebilir olup olmadığına karar verme çalışmalarını da beraberinde getirmektedir, yani yeni kavramla eski “reaktif” yaklaşımlar yerini “proaktif” yaklaşımlara bırakmıştır.</p>
					<p>Çalışma alanlarında uygulamaya konulan iş sağlığı ve güvenliği yaklaşımının en etkin yöntemlerinden biride risk analizi/risk değerlendirmesi çalışmalarının yapılmasıdır. Çalışma alanında karşılaşabileceğimiz risklere dair analizin yapılarak, yönetimsel bazda aktifliğinin sağlanması iş sağlığı ve güvenliği çalışmalarının ana kollarından biridir.</p>
					<h2>Risk Analizi ve Yönetimi</h2>
					<p>Risk analizi işyerinde sağlık ve güvenliği etkileyebilecek unsurların kontrolü amacıyla geliştirilmiş sistematik bir yaklaşımdır. Esas amacı, işyerindeki mevcut riskleri belirlemek, önceliklendirmek, riski ortadan kaldırmaya öncelik vererek uygun önlemlerle riski kontrol altına almaktır.</p>
					<p>İş Sağlığı ve Güvenliği Risk yönetiminin amacı; iş kazaları ve meslek hastalıklarını oluşturan nedenler ve bunları etkileyen faktörler ile ilgili mümkün olan en geçerli ve doğru bilgiyi toplayarak görünmeyen tehlikelerin ortaya çıkmasını engellemek için etkili bir güvenlik ağı kurmaktır. İyi bir risk analizi, doğabilecek kazalardan korunma açısından büyük değer taşır ve görünmeyen tehlikelerin ortaya çıkarılmasını, etkili güvenlik önlemlerinin alınmasını sağlar.</p>
					<p>Bir risk ile karşı karşıya kalındığında ilk önce yapılması gereken şey o riski oluşturacak kaynak olayı ve riskin etkilerini belirlemektir. Böylece riski tanımak daha kolaylaşır ve riske karşı ne gibi önlemler alınacağı daha rahat bir şekilde ortaya konulabilir. Ayrıca, kötü belirlenmiş risklerin daha başka risklerin doğmasına neden olacağını belirtmekte fayda vardır.</p>
