					<p><b>Yaşam alanınızda doğru ısıyı bulmaya çalışın.</b> Düşük ortam ısısı kadar yüksek ısı da çalışmak ve yaşamak için iç ortam kalitesini olumsuz etkilemektedir. Yapılan iş ve giyilen kıyafete bağlı olarak değişen ortam ısısı kişisel farklılıklar da gösterebilir. Kendiniz için doğru ısıyı bulmaya çalışmanız önemlidir.</p>
					<p><b>Yaşam alanında ve işyerinde temiz su kullanımı temel iç ortam kalitesi kriterlerinden birisidir.</b> Kullandığınız su kaynağının temizliğinin belirli aralıklarla kontrol edildiğinden emin olunmalıdır. Musluk suyu kullanıldığı hallerde kamu otoritelerinden, paket sular kullanılıyorsa üreticiden bu kontrol sonuçları talep edilmelidir. Arıtma cihazı benzeri su kalitesini artırmaya yönelik girişimler yapılmışsa belirli periyotlarla su ölçümleri yapılarak uygun vasıfta su elde edildiğine emin olunmalıdır.</p>
