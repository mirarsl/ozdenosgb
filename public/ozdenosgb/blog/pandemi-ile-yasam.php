					<h2  class="first-header">Koronavirüs(Covid-19) Bilgileri</h2>
					<p>31 Aralık 2019’da Dünya Sağlık Örgütü (DSÖ) Çin Ülke Ofisi, Çin’in Hubei eyaletinin Wuhan şehrinde etiyolojisi bilinmeyen pnömoni vakalarını bildirmiştir. 7 Ocak 2020’de etken daha önce insanlarda tespit edilmemiş yeni bir koronavirüs (2019- nCoV) olarak tanımlanmıştır. Daha sonra 2019-nCoV hastalığının adı COVID-19 olarak kabul edilmiş, virüs SARS CoV’e yakın benzerliğinden dolayı SARS-CoV-2 olarak isimlendirilmiştir.</p>
					<p>Dünya Sağlık Örgütü, COVID-19 salgınını 30 Ocak’ta “uluslararası boyutta halk sağlığı acil durumu” olarak sınıflandırmış, ilk salgının başladığı Çin dışında 113 ülkede COVID-19 vakalarının görülmesi, virüsün yayılımı ve şiddeti nedeniyle 11 Mart’ta küresel salgın (pandemi) olarak tanımlamıştır. Ülkemizde ilk COVID-19 vakası 11 Mart’ta görülmüştür.</p>
					<p>Enfeksiyon zinciri kaynak, bulaşma yolu ve duyarlı kişilerden oluşmaktadır.</p>
					<h2>Virüsün Kaynağı</h2>
					<p>Kaynak SARS-CoV-2’nin rezervuarı hala araştırılmaktadır. COVID-19 için mevcut tüm kanıtlar, SARS-CoV-2’nin zoonotik bir kaynağı olduğunu düşündürmektedir. İnsandan insana bulaş özelliği kazanması nedeniyle COVID-19’da kaynak semptomatik/asemptomatik COVID-19 pozitif kişilerdir.</p>
					<h2>Bulaşma Yolu</h2>
					<p>Hastalık esas olarak damlacık yoluyla bulaşmaktadır. Ayrıca hasta bireylerin öksürme, hapşırma yoluyla ortaya saçtıkları damlacıklara diğer kişilerin elleri ile temas etmesi sonrasında ellerini ağız, burun veya göz mukozasına götürmesi ve temas etmesi ile bulaşmaktadır.</p>
					<p>Asemptomatik kişilerin solunum yolu salgılarında da virüs tespit edilebildiğinden bu kişiler bulaştırıcı olabilmektedir. Genel olarak inkubasyon süresi 2-14 gün arasında değişmektedir. COVID-19’un bulaştırıcılık süresi kesin olarak bilinmemektedir. Semptomatik dönemden 1-2 gün önce başladığı düşünülmektedir.</p>
					<h2>Bulaştırıcılık, Viral Yük</h2>
					<p>Viral saçılım semptomlarının başlamasından 1-2 gün önce başlamakta ve boğaz sürüntülerinde semptomların ortaya çıkış döneminde viral yük doruğa çıkmakta ilk yedi gün içersinde hızla düşmekle birlikte ikinci haftanın ötesine uzayabilmektedir. Bazı çalışmalarda semptomatik ve asemptomatik/minimal semptomatik hastalarda viral yüklerin benzer bulunması asemptomatik kişilerin de bulaşmada rollerinin olduğuna işaret etmektedir. Öte yandan ağır vakalarda viral yükün daha yüksek olduğuna bildiren çalışmalar da mevcuttur. Yaşlılarda viral yük daha fazladır. Viral yük hastalık şiddeti ve prognozu açısından önemli bir belirteçtir. Viral yükün ağır olgularda hafif olgulardan 60 kat fazla olduğu gösterilmiştir.  Bulaşta sadece virüsün aktivitesinin devam etmesi değil, temasın süresinin de önemli olduğu unutulmamalıdır.</p>
					<h2>Duyarlı Kişi/Konakçı</h2>
					<p>COVID-19’da toplumun tümü duyarlıdır. Sağlık çalışanları etkenle karşılaşma yönünden en riskli meslek grubudur. Erkekler, 50 yaşın üstünde olan kişiler, komorbiditesi (Hipertansiyon, Kalp Hastalığı, Diyabet, Malignite, KOAH, Böbrek Hastalığı vb.) olan kişiler, mevsimlik tarım işçileri ile bakım ve rehabilitasyon merkezleri, okullar, kışlalar, ceza ve tevkif evleri ve göçmen kamplarında yaşayanlar COVID-19 açısından hassas gruplardır.</p>
					<h2>Koronavirüs(Covid-19) Belirtileri</h2>
					<p>COVID-19 belirtileri COVID-19'a neden olan SARS-CoV-2 enfeksiyonu, hafif ila şiddetli arasında değişen hastalıklara neden olabilir ve bazı durumlarda ölümcül olabilir. Semptomlar tipik olarak ateş, öksürük ve nefes darlığı içerir. Virüs bulaşmış bazı insanlar diğer solunum dışı semptomlar yaşadıklarını bildirmiştir. Asemptomatik vakalar olarak adlandırılan diğer kişilerde hiç semptom görülmemiştir. CDC'ye göre, COVID-19 semptomları maruziyetten 2 gün veya 14 gün sonra ortaya çıkabilir.</p>
					<p>Bu virüs grubuna bağlı virüsler, genellikle soğuk algınlığına benzer şekilde, hafif ve orta derecede üst solunum yolu hastalığı olan insanları hasta edebilir.</p>
					<p>Pandemi sürecinde yapılan çalışmaların sonucunda COVID-19’un doğal seyrine ilişkin bilgilere yenileri eklenmektedir. Enfeksiyonun yaygın belirtileri solunum semptomları, ateş, öksürük ve dispnedir. Başağrısı, boğaz ağrısı, burun akıntısı, kas ve eklem ağrıları, aşırı halsizlik, yeni ortaya çıkan koku ve tat alma duyusu kaybı, ishal gibi belirtiler de görülebilmektedir. Hastalık asemptomatik geçirilebilmekle birlikte, ciddi vakalarda, pnömoni, ağır akut solunum yolu enfeksiyonu, böbrek yetmezliği ve hatta ölüm gelişebilmektedir. </p>
					<h2>Koronavirüs(Covid-19) Nasıl Yayılır?</h2>
					<p>İlk insan COVID-19 vakaları muhtemelen enfekte hayvanlara maruz kalmayla sonuçlansa da enfekte insanlar SARS-CoV-2'yi diğer insanlara yayabilir. </p>
					<p>Virüsün, esas olarak insandan insana yayıldığı düşünülmektedir:</p>
					<ul>
						<li>Birbirleriyle yakın temas halinde olan kişiler arasında (yaklaşık 2 mt içinde).</li>
						<li>Enfekte bir kişi öksürdüğünde veya hapşırdığında üretilen solunum damlacıkları ile. Bu damlacıklar, yakınlarda bulunan insanların muhtemelen akciğerlere hava solunan ağızlarına veya burunlarına inebilir.</li>
					</ul>
					<p>Bir kişinin, üzerinde SARS-CoV-2 bulunan bir yüzeye veya nesneye dokunduktan sonra kendi ağzına, burnuna veya gözlerine dokunarak COVID-19'u elde etmesi mümkün olabilir, ancak bunun birincil virüsün yayılma şekli olduğu düşünülmemektedir. </p>
					<p>İnsanların, en semptomatik oldukları anda (yani ateş, öksürük ve / veya nefes darlığı) en bulaşıcı oldukları düşünülmektedir. </p>
